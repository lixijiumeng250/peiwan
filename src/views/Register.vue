                placeholder="请输入用户名"
                required
                class="form-input"
                :class="{ 
                  'error': errors.username,
                  'username-success': usernameValidationStatus.status === 'success',
                  'username-error': usernameValidationStatus.status === 'error'
                }"
                @blur="validateUsername"
              />
              <div class="validation-icon" v-if="usernameValidationStatus.loading || usernameValidationStatus.status">
                <span v-if="usernameValidationStatus.loading" class="loading-spinner-small"></span>
                <span v-else-if="usernameValidationStatus.status === 'success'" class="success-icon">✓</span>
                <span v-else-if="usernameValidationStatus.status === 'error'" class="error-icon">✗</span>
              </div>
            </div>
            <span v-if="usernameValidationStatus.message" 
                  class="validation-message" 
                  :class="usernameValidationStatus.status === 'success' ? 'success-message' : 'error-message'">
              {{ usernameValidationStatus.message }}
            </span>
          </div>
          
          <div class="form-group">
            <label for="realName">真实姓名</label>
            <input
              id="realName"
              type="text"
              v-model="registerForm.realName"
              placeholder="请输入真实姓名"
              required
              class="form-input"
              :class="{ 'error': errors.realName }"
            />
            <span v-if="errors.realName" class="error-message">{{ errors.realName }}</span>
          </div>
          
          <div class="form-group">
            <label for="phone">手机号码</label>
            <div class="phone-input-wrapper">
              <input
                id="phone"
                type="text"
                v-model="registerForm.phone"
                placeholder="请输入手机号码（可选）"
                class="form-input"
                :class="{ 
                  'error': errors.phone,
                  'phone-success': phoneValidationStatus.status === 'success',
                  'phone-error': phoneValidationStatus.status === 'error'
                }"
                @blur="validatePhone"
              />
              <div class="validation-icon" v-if="phoneValidationStatus.loading || phoneValidationStatus.status">
                <span v-if="phoneValidationStatus.loading" class="loading-spinner-small"></span>
                <span v-else-if="phoneValidationStatus.status === 'success'" class="success-icon">✓</span>
                <span v-else-if="phoneValidationStatus.status === 'error'" class="error-icon">✗</span>
              </div>
            </div>
            <span v-if="phoneValidationStatus.message" 
                  class="validation-message" 
                  :class="phoneValidationStatus.status === 'success' ? 'success-message' : 'error-message'">
              {{ phoneValidationStatus.message }}
            </span>
          </div>
          
          <div class="form-group">
            <label for="password">密码</label>
            <div class="password-input-wrapper">
              <input
                id="password"
                :type="showPassword ? 'text' : 'password'"
                v-model="registerForm.password"
                placeholder="请输入密码"
                required
                class="form-input"
                :class="{ 'error': errors.password }"
              />
              <button
                type="button"
                class="password-toggle"
                @click="togglePasswordVisibility"
                :title="showPassword ? '隐藏密码' : '显示密码'"
              >
                {{ showPassword ? '👁️' : '👁️‍🗨️' }}
              </button>
            </div>
            <span v-if="errors.password" class="error-message">{{ errors.password }}</span>
          </div>
          
          <div class="form-group">
            <label for="confirmPassword">确认密码</label>
            <div class="password-input-wrapper">
              <input
                id="confirmPassword"
                :type="showConfirmPassword ? 'text' : 'password'"
                v-model="registerForm.confirmPassword"
                placeholder="请再次输入密码"
                required
                class="form-input"
                :class="{ 'error': errors.confirmPassword }"
              />
              <button
                type="button"
                class="password-toggle"
                @click="toggleConfirmPasswordVisibility"
                :title="showConfirmPassword ? '隐藏密码' : '显示密码'"
              >
                {{ showConfirmPassword ? '👁️' : '👁️‍🗨️' }}
              </button>
            </div>
            <span v-if="errors.confirmPassword" class="error-message">{{ errors.confirmPassword }}</span>
          </div>
          
          <div class="form-options">
            <label class="checkbox-label">
              <input type="checkbox" v-model="agreeTerms" required />
              <span class="checkmark"></span>
              我已阅读并同意 <a href="#" class="terms-link">服务条款</a> 和 <a href="#" class="terms-link">隐私政策</a>
            </label>
          </div>
          
          <button type="submit" class="auth-button" :disabled="isLoading || !agreeTerms">
            <span v-if="isLoading" class="loading-spinner"></span>
            {{ isLoading ? '注册中...' : '注册' }}
          </button>
        </form>
        
        <div class="auth-footer">
          <p>已有账号？ <router-link to="/login" class="auth-link">立即登录</router-link></p>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { ElMessage } from 'element-plus'
import authStore from '../store/auth'
import { checkUsernameAvailability, checkPhoneAvailability } from '../api/auth'

export default {
  name: 'Register',
  data() {
    return {
      registerForm: {
        username: '',
        realName: '',
        phone: '',
        password: '',
        confirmPassword: ''
      },
      agreeTerms: false,
      errors: {},
      showPassword: false,
      showConfirmPassword: false,
      usernameCheckTimeout: null,
      phoneCheckTimeout: null,
      usernameValidationStatus: {
        loading: false,
        status: '', // 'success', 'error', ''
        message: ''
      },
      phoneValidationStatus: {
        loading: false,
        status: '', // 'success', 'error', ''
        message: ''
      }
    }
  },
  computed: {
    isLoading() {
      return authStore.getters.isLoading.value
    },
    errorMessage() {
      return authStore.getters.error.value
    }
  },
  watch: {
    // 实时验证 - 防抖处理
    'registerForm.username'() {
      this.debouncedValidateUsername()
    },
    'registerForm.realName'() {
      this.validateRealName()
    },
    'registerForm.phone'() {
      this.debouncedValidatePhone()
    },
    'registerForm.password'() {
      this.validatePassword()
      // 密码变化时重新验证确认密码
      if (this.registerForm.confirmPassword) {
        this.validateConfirmPassword()
      }
    },
    'registerForm.confirmPassword'() {
      this.validateConfirmPassword()
    }
  },
  methods: {
    // 防抖验证用户名
    debouncedValidateUsername() {
      clearTimeout(this.usernameCheckTimeout)
      this.usernameCheckTimeout = setTimeout(() => {
        this.validateUsername()
      }, 500)
    },
    
    // 防抖验证手机号
    debouncedValidatePhone() {
      clearTimeout(this.phoneCheckTimeout)
      this.phoneCheckTimeout = setTimeout(() => {
        this.validatePhone()
      }, 500)
    },
    
    
    // 验证用户名
    async validateUsername() {
      const username = this.registerForm.username?.trim()
      
      if (!username) {
        this.usernameValidationStatus = {
          loading: false,
          status: '',
          message: ''
        }
        return
      }
      
      if (username.length < 2) {
        this.usernameValidationStatus = {
          loading: false,
          status: 'error',
          message: '用户名至少需要2个字符'
        }
        return
      }
      
      if (username.length > 20) {
        this.usernameValidationStatus = {
          loading: false,
          status: 'error',
          message: '用户名不能超过20个字符'
        }
        return
      }
      
      if (!/^[a-zA-Z0-9_\u4e00-\u9fa5]+$/.test(username)) {
        this.usernameValidationStatus = {
          loading: false,
          status: 'error',
          message: '用户名只能包含字母、数字、下划线和中文'
        }
        return
      }
      
      // 调用API检查用户名可用性
      this.usernameValidationStatus.loading = true
      console.log('正在检查用户名可用性:', username)
      
      try {
        const response = await checkUsernameAvailability(username)
        console.log('用户名验证API完整响应:', response)
        console.log('响应code:', response?.code)
        console.log('响应data:', response?.data)
        console.log('响应类型:', typeof response?.data)
        
        // 检查响应格式 - 兼容 code 0 和 200
        const isSuccessCode = response && (response.code === 0 || response.code === 200)
        
        if (isSuccessCode && response.data === true) {
          this.usernameValidationStatus = {
            loading: false,
            status: 'success',
            message: '用户名可用'
          }
        } else if (isSuccessCode && response.data === false) {
          this.usernameValidationStatus = {
            loading: false,
            status: 'error',
            message: '用户名已被其他用户使用'
          }
        } else {
          // 响应格式不符合预期
          console.warn('用户名验证API响应格式异常:', response)
          this.usernameValidationStatus = {
            loading: false,
            status: 'error',
            message: '用户名验证服务异常，请稍后重试'
          }
        }
      } catch (error) {
        console.error('检查用户名可用性失败:', error)
        console.error('错误详情:', error.response?.data || error.message)
        this.usernameValidationStatus = {
          loading: false,
          status: 'error',
          message: '检查用户名失败，请稍后重试'
        }
      }
    },
    
    // 验证手机号
    async validatePhone() {
      const phone = this.registerForm.phone?.trim()
      
      if (!phone) {
        this.phoneValidationStatus = {
          loading: false,
          status: '',
          message: ''
        }
        return
      }
      
      // 手机号格式验证（宽松验证，允许多种格式）
      if (phone.length < 3 || phone.length > 20) {
        this.phoneValidationStatus = {
          loading: false,
          status: 'error',
          message: '手机号长度不正确'
        }
        return
      }
      
      // 只包含数字、+号、-号、空格、括号
      if (!/^[\d\+\-\s\(\)]+$/.test(phone)) {
        this.phoneValidationStatus = {
          loading: false,
          status: 'error',
          message: '手机号格式不正确，请输入有效的手机号'
        }
        return
      }
      
      // 调用API检查手机号可用性
      this.phoneValidationStatus.loading = true
      console.log('正在检查手机号可用性:', phone)
      
      try {
        const response = await checkPhoneAvailability(phone)
        console.log('手机号验证API完整响应:', response)
        console.log('响应code:', response?.code)
        console.log('响应data:', response?.data)
        console.log('响应类型:', typeof response?.data)
        
        // 检查响应格式 - 兼容 code 0 和 200
        const isSuccessCode = response && (response.code === 0 || response.code === 200)
        
        if (isSuccessCode && response.data === true) {
          this.phoneValidationStatus = {
            loading: false,
            status: 'success',
            message: '手机号可用'
          }
        } else if (isSuccessCode && response.data === false) {
          this.phoneValidationStatus = {
            loading: false,
            status: 'error',
            message: '手机号已被其他用户使用，请更换手机号'
          }
        } else {
          // 响应格式不符合预期
          console.warn('手机号验证API响应格式异常:', response)
          this.phoneValidationStatus = {
            loading: false,
            status: 'error',
            message: '手机号验证服务异常，请稍后重试'
          }
        }
      } catch (error) {
        console.error('检查手机号可用性失败:', error)
        console.error('错误详情:', error.response?.data || error.message)
        this.phoneValidationStatus = {
          loading: false,
          status: 'error',
          message: '检查手机号失败，请稍后重试'
        }
      }
    },
    
    // 验证真实姓名
    validateRealName() {
      const realName = this.registerForm.realName
      
      if (!realName) {
        delete this.errors.realName
        return
      }
      
      if (realName.length < 2) {
        this.errors.realName = '真实姓名至少需要2个字符'
      } else if (realName.length > 10) {
        this.errors.realName = '真实姓名不能超过10个字符'
      } else if (!/^[\u4e00-\u9fa5a-zA-Z\s]+$/.test(realName)) {
        this.errors.realName = '真实姓名只能包含中文、英文字母和空格'
      } else {
        delete this.errors.realName
      }
    },
    
    
    // 验证密码
    validatePassword() {
      const password = this.registerForm.password
      
      if (!password) {
        delete this.errors.password
        return
      }
      
      if (password.length > 20) {
        this.errors.password = '密码不能超过20个字符'
      } else {
        delete this.errors.password
      }
    },
    
    // 验证确认密码
    validateConfirmPassword() {
      const confirmPassword = this.registerForm.confirmPassword
      
      if (!confirmPassword) {
        delete this.errors.confirmPassword
        return
      }
      
      if (confirmPassword !== this.registerForm.password) {
        this.errors.confirmPassword = '两次输入的密码不一致'
      } else {
        delete this.errors.confirmPassword
      }
    },
    
    // 验证整个表单
    async validateForm() {
      // 先进行基本验证
      this.validateRealName()
      this.validatePassword()
      this.validateConfirmPassword()
      
      // 异步验证用户名和手机号
      const usernamePromise = this.validateUsername()
      const phonePromise = this.validatePhone()
      
      await Promise.all([usernamePromise, phonePromise])
      
      // 检查必填字段
      const requiredFields = ['username', 'realName', 'password', 'confirmPassword']
      requiredFields.forEach(field => {
        if (!this.registerForm[field]) {
          this.errors[field] = '此字段为必填项'
        }
      })
      
      // 检查验证状态
      if (this.usernameValidationStatus.status === 'error') {
        return false
      }
      
      if (this.registerForm.phone && this.phoneValidationStatus.status === 'error') {
        return false
      }
      
      // 检查是否有必填字段错误
      return Object.keys(this.errors).length === 0
    },
    
    // 清除错误信息
    clearErrors() {
      this.errors = {}
      authStore.actions.clearError()
    },
    
    // 处理注册
    async handleRegister() {
      // 清除之前的错误
      authStore.actions.clearError()
      
      // 验证表单
      const isValid = await this.validateForm()
      if (!isValid) {
        ElMessage({
          message: '请修正表单中的错误后再提交',
          type: 'warning',
          duration: 3000,
          showClose: true
        })
        return
      }
      
      if (!this.agreeTerms) {
        ElMessage({
          message: '请同意服务条款和隐私政策',
          type: 'warning',
          duration: 3000,
          showClose: true
        })
        return
      }
      
      try {
        // 调用注册API
        const result = await authStore.actions.register({
          username: this.registerForm.username,
          realName: this.registerForm.realName,
          phone: this.registerForm.phone || '', // 手机号可选
          password: this.registerForm.password,
          confirmPassword: this.registerForm.confirmPassword
        })
        
        if (result.success) {
          // 注册成功
          ElMessage({
            message: `注册成功！欢迎加入，${result.user?.username || '用户'}！正在为您跳转到登录页面...`,
            type: 'success',
            duration: 4000,
            showClose: true
          })
          
          // 增加延迟时间，确保后端数据已经写入
          setTimeout(() => {
            this.$router.push({
              path: '/login',
              query: { 
                username: this.registerForm.username,
                fromRegister: 'true',
                tip: '注册成功，请使用刚才的用户名和密码登录'
              }
            })
          }, 2500)
        } else {
          // 注册失败，显示错误消息
          ElMessage({
            message: result.message || '注册失败，请稍后重试',
            type: 'error',
            duration: 4000,
            showClose: true
          })
        }
        
      } catch (error) {
        console.error('注册过程发生错误:', error)
        ElMessage({
          message: '注册过程中发生错误，请稍后重试',
          type: 'error',
          duration: 4000,
          showClose: true
        })
      }
    },
    
    // 切换密码显示状态
    togglePasswordVisibility() {
      this.showPassword = !this.showPassword
    },
    
    // 切换确认密码显示状态
    toggleConfirmPasswordVisibility() {
      this.showConfirmPassword = !this.showConfirmPassword
    },
    
    // 快速填写表单（演示用）
    quickFill() {
      this.registerForm = {
        username: 'testuser' + Date.now().toString().slice(-4),
        realName: '测试用户',
        phone: '13800138000',
        password: 'test123456',
        confirmPassword: 'test123456'
      }
      this.agreeTerms = true
    }
  },
  
  // 组件挂载时初始化
  mounted() {
    // 如果已经登录，直接跳转
    if (authStore.getters.isAuthenticated.value) {
      this.$router.push('/')
      return
    }
    
    // 清除之前的错误状态
    authStore.actions.clearError()
  },
  
  // 组件卸载时清理
  beforeUnmount() {
    clearTimeout(this.usernameCheckTimeout)
    clearTimeout(this.phoneCheckTimeout)
    authStore.actions.clearError()
  }
}
</script>

